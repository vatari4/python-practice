
numbers = ['1', '3', '4', '2'] #создаем массив

numbers.sort() #сортируем массив

print(numbers) #расспечатываем массив


numbers2 = ['1', '3', '4', '2'] #создаем массив

numbers3 = sorted(numbers2) #сортируем массив присваивая результат в новую переменную

print(numbers2) #расспечатываем исходный массив

print(numbers3) #расспечатываем отсортированный массив